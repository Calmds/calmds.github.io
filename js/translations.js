// 简化的翻译获取函数
function getTranslation(langCode, key) {
    // 如果请求的语言不存在，回退到英语
    const langData = Translations[langCode] || Translations['en'];

    // 分割键路径
    const keys = key.split('.');
    let value = langData;

    // 遍历键路径
    for (const k of keys) {
        if (value && typeof value === 'object' && k in value) {
            value = value[k];
        } else {
            // 如果找不到，尝试从英语获取
            const enValue = getNestedValue(Translations['en'], key);
            return enValue || key; // 返回键本身作为最后手段
        }
    }

    return value;
}

// 获取嵌套对象的值
function getNestedValue(obj, path) {
    if (!obj || !path) return undefined;

    const keys = path.split('.');
    let current = obj;

    for (const key of keys) {
        if (current[key] === undefined) {
            return undefined;
        }
        current = current[key];
    }

    return current;
}